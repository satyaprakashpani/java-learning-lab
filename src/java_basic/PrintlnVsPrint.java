package java_basic;

public class PrintlnVsPrint
{
    public static void main(String[] args) {
        System.out.println("Println executed here, new line");

        System.out.print("print executed ---, no new line");

        System.out.print("print executed ---, no new line");

        System.out.println("Println executed here, new line");
        System.out.print("print executed ---, no new line");
    }
}
